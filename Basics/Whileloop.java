public class Whileloop {
    public static void main(String[] args) {
        int i = 02;
        while (i < 5) {
            System.out.println("task within while loop");
            i++;
        }
        int j = 0;
        do {
            System.out.println("do while loop");
            j++;

        } while (j < 8);
    }
}
